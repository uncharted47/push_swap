#include "push_swap.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while ((str1[i] || str2[i]) && i < n)
	{
		if (str1[i] != str2[i])
		{
			if ((str1[i] - str2[i]) < 0)
				return (0);
			else if ((str1[i] - str2[i]) > 0)
				return (0);
		}
		i++;
	}
	return (1);
}
